#include <stdio.h>

// Визначимо розміри матриці як константи для зручності
#define ROWS 3
#define COLS 3

int main() {
  // 1. Оголошення та ініціалізація вихідної матриці A
  // Використовуємо тип float, оскільки в умові є дробове число 1.5
  float matrixA[ROWS][COLS] = {
      {3.0, -5.0, 7.0}, {1.0, 1.5, 15.0}, {45.0, -1.0, 8.0}};

  // 2. Оголошення масиву B для зберігання результатів
  float vectorB[ROWS];

  // 3. Основний алгоритм: пошук найменших елементів
  printf("Пошук найменших елементів у кожному рядку...\n");

  // Зовнішній цикл: проходимо по кожному рядку матриці
  for (int i = 0; i < ROWS; i++) {
    // Припускаємо, що перший елемент поточного рядка є найменшим
    float min_element = matrixA[i][0];

    // Внутрішній цикл: проходимо по всіх елементах цього рядка,
    // починаючи з другого, щоб порівняти їх з поточним мінімумом
    for (int j = 1; j < COLS; j++) {
      // Якщо знаходимо елемент, менший за поточний min_element,
      // оновлюємо min_element
      if (matrixA[i][j] < min_element) {
        min_element = matrixA[i][j];
      }
    }

    // 4. Записуємо знайдений мінімум у масив B
    vectorB[i] = min_element;
  }

  // 5. Виведення результату
  printf("\nСформований масив B з найменших елементів кожного рядка:\n");
  for (int i = 0; i < ROWS; i++) {
    // Використовуємо %.2f для гарного форматування дробових чисел
    printf("B[%d] = %.2f\n", i, vectorB[i]);
  }

  return 0;
}